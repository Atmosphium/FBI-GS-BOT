// İhracat

export const GelecekCeza = (uyarıSayısı: number): string => {
    if (uyarıSayısı < 3) return Cezalar [0]
    if (uyarıSayısı > 9) return 'Kurucularca özel olarak belirlenecek.'

    return Cezalar [uyarıSayısı - 2]
}

