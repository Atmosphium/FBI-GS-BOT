export const EmojiSayılar = (Sayı: number): string => {
    return Sayı.toString ()
        .replaceAll ('1', '1️⃣')
        .replaceAll ('2', '2️⃣')
        .replaceAll ('3', '3️⃣')
        .replaceAll ('4', '4️⃣')
        .replaceAll ('5', '5️⃣')
        .replaceAll ('6', '6️⃣')
        .replaceAll ('7', '7️⃣')
        .replaceAll ('8', '8️⃣')
        .replaceAll ('9', '9️⃣')
        .replaceAll ('0', '0️⃣')
}

